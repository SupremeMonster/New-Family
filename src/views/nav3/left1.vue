<p>你要爸妈做什么都行，只要你把英语多听几遍。</p>
<p>小编点评</p>
<p>家长一定要认清事实&mdash;&mdash;孩子也会抑郁。在残酷的事实面前，儿童的心理健康问题是该引起家长认真思考的时候了。孩子成才，除了智力开发以外，培养健康的心理，良好的社会适应能力，良好的心理防御方式更加重要。</p><br />
