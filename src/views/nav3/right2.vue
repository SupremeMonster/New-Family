<p><strong>&nbsp;老人健身四招</strong></p>
<p>定期做医学检查了解身体情况</p>
<p>潘翠环还提醒，老年人健身，以下几点一定要注意：</p>
<p>1.全面、定期做医学检查，了解自己的身体情况。</p>
<p>老年人应通过定期医学检查，了解锻炼前、锻炼中和锻炼一阶段后的健康状况，疾病进展和各脏器的功能水平，特别注意是否有心血管和脑血管系统的慢性疾病，以及是否有骨质疏松、骨关节方面的病变等，确保锻炼的安全性。</p>
<p>2.选择合适自己的运动方式。</p>
<p>对老年人的运动要求是低负重、低对抗性，舒缓且安静。</p>
<p>慢跑：要在沙地和软地上进行，注意控制时间和心率。</p>
<p>游泳：最好在室内，水温要求稍高一点，缓慢的游泳，不要求速度。</p>
<p>骑自行车：更加要求在安全的地方进行。</p>
<p>老年舞蹈和太极拳、柔力球：比较适合老年人运动。</p>
<p>琴棋书画：不仅能陶冶情操，而且也能锻炼身体。</p>
<p>3.对运动强度的选择应因人而异，循序渐进。</p>
<p>对于体质较弱或初次锻炼者而言，开始的运动要低强度、短时间，用2周左右的时间观察其反应，可能会有几天的肌肉酸痛、劳累等反应，但会很快消失。适应后再增加运动量，小幅度增加运动量后再有一段适应期，呈渐进式训练，不要追求直线增加运动量。</p>
<p>当经过一阶段的适应性运动后，锻炼要想达到对心肺功能进行有效刺激的目的，强度就要达到一定的阈值，可根据以下公式计算运动后适宜心率：运动目标心率= （220-年龄-安静心率）&times;（40~60％）+安静心率。</p>
<p>4.要有规律、持之以恒地参加锻炼</p>
<p>运动对机体的刺激是有累积效应的，锻炼达到改善健康的最佳效果需要数周至数月的时间，但如果停止锻炼两周，便呈明显下降趋势。</p>
<p>美国运动医学会推荐，老年人如果从事中等强度的有氧运动，每天累计至少30~60分钟，每次至少10分钟以上，每周累计150~300分钟。</p>
<p>如果从事较大强度的有氧运动，每天至少20~30分钟，或每周累计75~150分钟。</p>
<p>在进行有氧锻炼的同时，辅以每周不少于2天的肌肉力量训练，练习包括8~12个/次的重复动作或者持续到在没有帮助时难以完成下一个重复动作为止。</p>
