<p>文/羊城晚报记者余燕红通讯员李晓姗</p>
<p>近年来，随着人口的老龄化，我国帕金森病的患病率逐渐攀升，在65岁以上人群中达到了1.7%，患者超过300万，其平均发病年龄为60岁左右。但是帕金森病在我国仍处于认知度低、就诊率低、诊断率低的&ldquo;三低&rdquo;状态。近半患者初期没有意识到身患此病而造成许多人延误诊治。本期健康周刊专门针对帕金森病的早发现、早诊断、早治疗，采访各大医院专家，给读者详细解答。</p>
<p>帕金森病是老年人常见的慢性病，此病虽然不会危及生命，但如不及时治疗，会严重影响病人的生活。南方医院神经内科主任医师王群介绍，帕金森病常以手脚颤抖、肢体僵硬、行动迟缓为主要表现，早期经常被误认为是正常的老化或者被误诊为其他疾病。如何早期发现帕金森病呢？以下7大症状要警惕！</p>
<p style="text-align: center;"><img src="http://p0.ifengimg.com/cmpp/2017/05/02/08/e32b7745-3613-4107-a8e1-99f1387d6706_size33_w400_h257.jpg" alt="" /></p>
